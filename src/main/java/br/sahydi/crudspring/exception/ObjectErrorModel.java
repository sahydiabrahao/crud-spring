package br.sahydi.crudspring.exception;

public class ObjectErrorModel {

    //Atributos
    private String error;
    private String code;

    //Get Set
    public String getError() {
        return error;
    }

    public void setError(String error) {
        this.error = error;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

}
    
